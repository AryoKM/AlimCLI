def cowsay(message):
    bubble_width = len(message) + 2
    bubble_top = " " + "_" * bubble_width
    bubble_middle = f"< {message} >"
    bubble_bottom = " " + "-" * bubble_width

    cow = r"""
        \   ^___^
         \  (u u)\_______
            (_w_)\       )\/\
                ||----w |
                ||     ||
    """

    result = f"{bubble_top}\n{bubble_middle}\n{bubble_bottom}{cow}"
    print(result)




